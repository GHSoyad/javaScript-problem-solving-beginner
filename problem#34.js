// Write a function that will take a number as input parameter and it will return the square of the number. Like if you input parameter as 5 it will return 25.


function numSquare(number) {

    return number ** 2;
}

console.log(numSquare(5));
console.log(numSquare(2));
console.log(numSquare(10));
console.log(numSquare(25));
console.log(numSquare(46));