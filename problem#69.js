// Write a function called centimeterToMeter(). This function input it in centimeter and return meter.


function centimeterToMeter(centimeter) {

    let meter = centimeter / 100;     // Centimeter to meter formula

    return meter;
}

console.log(centimeterToMeter(1));
console.log(centimeterToMeter(100));
console.log(centimeterToMeter(581));
console.log(centimeterToMeter(2864));
console.log(centimeterToMeter(5000));